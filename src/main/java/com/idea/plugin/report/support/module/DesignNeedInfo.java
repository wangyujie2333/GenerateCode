package com.idea.plugin.report.support.module;

public class DesignNeedInfo {
    private String need;
    private String content;
    private String page;
    private String design;
    private String classstruct;
    private String datatruct;
    private String designlogic;
    private String backend;
    private String frontend;
    private String testscope;

    public String getNeed() {
        return need;
    }

    public void setNeed(String need) {
        this.need = need;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getPage() {
        return page;
    }

    public void setPage(String page) {
        this.page = page;
    }

    public String getDesign() {
        return design;
    }

    public void setDesign(String design) {
        this.design = design;
    }

    public String getClassstruct() {
        return classstruct;
    }

    public void setClassstruct(String classstruct) {
        this.classstruct = classstruct;
    }

    public String getDatatruct() {
        return datatruct;
    }

    public void setDatatruct(String datatruct) {
        this.datatruct = datatruct;
    }

    public String getDesignlogic() {
        return designlogic;
    }

    public void setDesignlogic(String designlogic) {
        this.designlogic = designlogic;
    }

    public String getBackend() {
        return backend;
    }

    public void setBackend(String backend) {
        this.backend = backend;
    }

    public String getFrontend() {
        return frontend;
    }

    public void setFrontend(String frontend) {
        this.frontend = frontend;
    }

    public String getTestscope() {
        return testscope;
    }

    public void setTestscope(String testscope) {
        this.testscope = testscope;
    }
}
