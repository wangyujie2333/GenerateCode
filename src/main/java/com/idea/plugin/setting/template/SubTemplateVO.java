package com.idea.plugin.setting.template;


public class SubTemplateVO {
    public String wordTemplate;
    public String mdTemplate;
    public String keyTemplate;
    public String wordTranslate;
    public String sqlTemplate;
    public String javatemplate;

    public SubTemplateVO() {
    }

    public String getWordTemplate() {
        return wordTemplate;
    }

    public void setWordTemplate(String wordTemplate) {
        this.wordTemplate = wordTemplate;
    }

    public String getMdTemplate() {
        return mdTemplate;
    }

    public void setMdTemplate(String mdTemplate) {
        this.mdTemplate = mdTemplate;
    }

    public String getKeyTemplate() {
        return keyTemplate;
    }

    public void setKeyTemplate(String keyTemplate) {
        this.keyTemplate = keyTemplate;
    }

    public String getWordTranslate() {
        return wordTranslate;
    }

    public void setWordTranslate(String wordTranslate) {
        this.wordTranslate = wordTranslate;
    }

    public String getSqlTemplate() {
        return sqlTemplate;
    }

    public void setSqlTemplate(String sqlTemplate) {
        this.sqlTemplate = sqlTemplate;
    }

    public String getJavatemplate() {
        return javatemplate;
    }

    public void setJavatemplate(String javatemplate) {
        this.javatemplate = javatemplate;
    }
}
